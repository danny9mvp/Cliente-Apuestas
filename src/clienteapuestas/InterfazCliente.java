    public void enviarAccion(String accion){
        DataOutputStream dos=null;
        try {            
            dos = new DataOutputStream(socket.getOutputStream());
            dos.flush();
            dos.writeUTF(accion);             
            System.out.println("Mensaje Enviado");
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
    
